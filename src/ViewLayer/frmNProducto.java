    public frmNProducto(int idProducto) {//Crear otro constructor, el cual sera llamado cuando el boton de modificar sea presionado, entonces nos vamos ahora al btnModificarActionPerformed que esta en la clase frmProductos**
        initComponents();
        this.idProducto = idProducto;//se le pone la unica propiedad que es idProducto pero es la unica propiedad que ocupa para poder llamar al metodo GetById() y este metodo lo que hace es poblar las otras propiedades con la informacion de la base de datos
        producto.setIdProducto(idProducto);
        producto.GetById();//recuerda que este metodo lo que hacia era consultar en base de datos y llenar las mismas propiedades de este objeto de la misma clase
        txtNombre.setText(producto.getNombre());
        txtCaducidad.setText(producto.getCaducidad());
        txtStock.setText("" + producto.getStock());
        txtFarmacia.setText("" + producto.getIdFarmacia());
        Farmacia farm = new Farmacia();
        DefaultTableModel res = farm.GetAllModel();
        int NumFilas = res.getRowCount();
        int tablaIdFarm[];
        tablaIdFarm = new int[NumFilas];
        String tablaNombreFarm[];
        tablaNombreFarm = new String[NumFilas];
        int indexSelected = 0;
        for (int i = 0; i < NumFilas; i++) {
            tablaIdFarm[i] = (int) res.getValueAt(i, 0);
            tablaNombreFarm[i] = (String) res.getValueAt(i, 1);
            comboBoxIdFarmacia.addItem(tablaNombreFarm[i]);
            if (tablaIdFarm[i] == producto.getIdFarmacia()) {
                indexSelected = i;
            }

        }
        comboBoxIdFarmacia.setSelectedIndex(indexSelected);
        this.tablaIdFarmClass = new int[NumFilas];
        this.tablaIdFarmClass = tablaIdFarm;

    }

