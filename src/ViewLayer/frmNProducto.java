    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        // TODO add your handling code here:
        if (producto.getIdProducto() > 0) {//osea si hay un Id es porque es Modificar
            producto.setNombre(txtNombre.getText());
            producto.setCaducidad(txtCaducidad.getText());
            producto.setStock(Integer.parseInt(txtStock.getText()));
            //producto.setIdFarmacia(Integer.parseInt(txtFarmacia.getText()));
            int idFarm = this.tablaIdFarmClass[(int) comboBoxIdFarmacia.getSelectedIndex()];
            producto.setIdFarmacia(idFarm);
            producto.setActivo(1);
            if (producto.Update()) {
                JOptionPane.showMessageDialog(null, "Producto actualizado correctamente");
                this.dispose();//cerramos
            }

        } else {// en caso de que no haiga Id es porque es agregar
            //Recordemos que nuestro paquete de negocios en la clase Producto hay un metodo de agregar (Add) que regresa un boolean osea si es mayor o igual a 1 es true menor de 1 es false, entonces hacemos lo siguiente:
            int idFarm = this.tablaIdFarmClass[(int) comboBoxIdFarmacia.getSelectedIndex()];
            if (new Producto(0,//le pasamos los parametros de la clase Producto por medio de parentesis
                    txtNombre.getText(),
                    txtCaducidad.getText(),//En esta parte se convirtio el tipo Date de la variable caducidad a String osea DATE to String
                    Integer.parseInt(txtStock.getText()),
                    //Integer.parseInt(txtFarmacia.getText()),
                    idFarm,
                    1).Add()) {
                JOptionPane.showMessageDialog(null, "Producto agregado correctamente");
                this.dispose();//cerramos el formulario despues de haber agrgado el producto; Entonces esto es lo que haria el codigo cuando se manda a llamar, entonces en el boton de Nuevo que estan en la interfaz de la clase frmProductos se va a mandar a llamar esta clase a traves de una instancia que va a estar en la clase de frmProductos en en btnNuevoActionPerformed
            }
        }

    }//GEN-LAST:event_btnOkActionPerformed

